#Write a python program that stores fruit names in a list named fruits. 
#Unpack the list into three variables x, y and z and then print the values of each variable.

# Storing fruit names in a list named fruits
fruits = ['apple', 'banana', 'cherry']

# Unpacking the list into three variables x, y, and z
x, y, z = fruits

# Printing the values of each variable
print("x =", x)
print("y =", y)
print("z =", z)